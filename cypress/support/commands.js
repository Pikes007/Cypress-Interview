Cypress.Commands.add("fillInForm", (selector, input) => {
    cy.get(selector).type(input);
});

Cypress.Commands.add(
    "waitForParentVisible",
    { prevSubject: "element" },
    (subject) => {
        return cy.wrap(subject)
            .parent()
            .should("be.visible");
    }
);

